import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { loadDomainConfig } from "./load-domain-config.mjs";

const defaultOutputPath = fileURLToPath(
  new URL("../../frontends/onboarding/runtime-config.js", import.meta.url),
);
const options = parseArguments(process.argv.slice(2));
const profiles = {
  development: loadDomainConfig("development"),
  production: loadDomainConfig("production"),
};
const generated = options.profile
  ? renderPinnedProfile(options.profile, profiles[options.profile])
  : renderHostnameSelection(profiles);

if (options.checkOnly) {
  if (readFileSync(options.outputPath, "utf8") !== generated) {
    throw new Error("frontends/onboarding/runtime-config.js is stale");
  }
} else {
  writeFileSync(options.outputPath, generated);
}

function parseArguments(args) {
  let checkOnly = false;
  let outputPath = defaultOutputPath;
  let profile = null;
  for (let index = 0; index < args.length; index += 1) {
    const argument = args[index];
    if (argument === "--check") {
      checkOnly = true;
    } else if (argument === "--profile") {
      profile = args[index + 1];
      index += 1;
    } else if (argument === "--output") {
      const candidate = args[index + 1];
      if (!candidate) throw new Error("--output requires a path");
      outputPath = resolve(candidate);
      index += 1;
    } else {
      throw new Error(`Unknown argument: ${argument}`);
    }
  }
  if (profile !== null && profile !== "development" && profile !== "production") {
    throw new Error("--profile must be development or production");
  }
  if (checkOnly && (profile !== null || outputPath !== defaultOutputPath)) {
    throw new Error("--check validates only the tracked multi-profile configuration");
  }
  return Object.freeze({ checkOnly, outputPath, profile });
}

function renderHostnameSelection(profileConfigurations) {
  return `// Generated by config/domains/generate-onboarding-config.mjs. Do not edit manually.\n(function configureSurplasseDomains() {\n  const profiles = Object.freeze(${JSON.stringify(profileConfigurations, null, 2)});\n  const hostname = window.location.hostname.toLowerCase().replace(/\\.$/u, "");\n  const loopbackHosts = new Set(["localhost", "127.0.0.1", "::1", "[::1]"]);\n  if (loopbackHosts.has(hostname)) {\n    throw new Error("Direct loopback previews are forbidden. Use the configured HTTPS development domain.");\n  }\n  const selectedProfile = Object.keys(profiles).find((profile) => {\n    const domain = profiles[profile].APP_BASE_DOMAIN;\n    return hostname === domain || hostname.endsWith(\`.\${domain}\`);\n  });\n  if (!selectedProfile) {\n    throw new Error("Hostname does not belong to a configured domain profile.");\n  }\n\n  window.SURPLASSE_DOMAIN_CONFIG = Object.freeze(\n    { ...profiles[selectedProfile], PROFILE: selectedProfile },\n  );\n})();\n`;
}

function renderPinnedProfile(profile, configuration) {
  return `// Generated by config/domains/generate-onboarding-config.mjs for ${profile}. Do not edit manually.\n(function configureSurplasseDomains() {\n  window.SURPLASSE_DOMAIN_CONFIG = Object.freeze(${JSON.stringify(
    { ...configuration, PROFILE: profile },
    null,
    2,
  )});\n})();\n`;
}
