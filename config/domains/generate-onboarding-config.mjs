import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

import { loadDomainConfig } from "./load-domain-config.mjs";

const outputPath = fileURLToPath(new URL("../../frontends/onboarding/runtime-config.js", import.meta.url));
const checkOnly = process.argv.includes("--check");
const generated = render({
  development: loadDomainConfig("development"),
  production: loadDomainConfig("production"),
});

if (checkOnly) {
  if (readFileSync(outputPath, "utf8") !== generated) {
    throw new Error("frontends/onboarding/runtime-config.js is stale");
  }
} else {
  writeFileSync(outputPath, generated);
}

function render(profiles) {
  return `// Generated by config/domains/generate-onboarding-config.mjs. Do not edit manually.\n(function configureSurplasseDomains() {\n  const profiles = Object.freeze(${JSON.stringify(profiles, null, 2)});\n  const hostname = window.location.hostname.toLowerCase().replace(/\\.$/u, "");\n  const developmentDomain = profiles.development.APP_BASE_DOMAIN;\n  const loopbackHosts = new Set(["localhost", "127.0.0.1", "::1", "[::1]"]);\n  if (loopbackHosts.has(hostname)) {\n    throw new Error("Direct loopback previews are forbidden. Use the configured HTTPS development domain.");\n  }\n  const isDevelopment =\n    hostname === developmentDomain ||\n    hostname.endsWith(\`.\${developmentDomain}\`);\n  const selectedProfile = isDevelopment ? "development" : "production";\n\n  window.SURPLASSE_DOMAIN_CONFIG = Object.freeze(\n    { ...profiles[selectedProfile], PROFILE: selectedProfile },\n  );\n})();\n`;
}
